// Funções para obter o ícone da viatura (MANTENHA ESTAS FUNÇÕES NO SEU guarnicoes-data.js)
function getViaturaIcon(tipoViatura) {
    switch (tipoViatura.toUpperCase()) {
        case 'ABT':
            return 'fas fa-fire-truck';
        case 'AR':
            return 'fas fa-ambulance';
        case 'AEM':
            return 'fas fa-truck-monster';
        case 'ATP':
            return 'fas fa-car-side';
        case 'ABC': // Para o Canil
            return 'fas fa-dog';
        case 'AT': // Camionete normal (do seu dado)
            return 'fas fa-truck-pickup';
        case 'ABTS': // Veículo de Salvamento (do seu dado)
            return 'fas fa-hand-holding-medical';
        case 'EAL': // Embarcação de Apoio Logístico
        case 'EIN': // Embarcação de Infiltração
        case 'EMN': // Embarcação Motor de Popa
            return 'fas fa-life-buoy'; // Bóia salva-vidas ou similar
        case 'DCCI': // Para COBOM-DCCI
            return 'fas fa-headset';
        case 'NAO CLASSIFICADO': // Caso viatura não tenha um tipo específico no prefixo
            return 'fas fa-truck';
        case 'NAVAL': // Para Aquática/Mergulho, se usado como tipo genérico
            return 'fas fa-life-ring';
        default:
            return 'fas fa-truck';
    }
}

